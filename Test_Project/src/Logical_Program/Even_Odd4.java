package Logical_Program;

public class Even_Odd4 
	{
		public static void main(String[] args) 
		{
			
			int j=2;
			while( j<=100)
			{
				if(j%2==0)
				
				{
					System.out.println(j+"Even no.");
				}
				else 
				{
					System.out.println(j+"Not even no.");
				}
				j++;
			}
			
		}

	}



